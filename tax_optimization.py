# ---------------- MERGE SORT ---------------- #
def merge_sort(arr):
    if len(arr) <= 1:
        return arr
    mid = len(arr)//2
    left = merge_sort(arr[:mid])
    right = merge_sort(arr[mid:])

    result = []
    i = j = 0

    while i < len(left) and j < len(right):
        if left[i]['priority'] < right[j]['priority']:
            result.append(left[i])
            i += 1
        else:
            result.append(right[j])
            j += 1

    result.extend(left[i:])
    result.extend(right[j:])
    return result


# ---------------- TAX SLABS ---------------- #
OLD_REGIME = [
    {'limit': 250000, 'rate': 0},
    {'limit': 500000, 'rate': 0.05},
    {'limit': 1000000, 'rate': 0.20},
    {'limit': float('inf'), 'rate': 0.30}
]

NEW_REGIME = [
    {'limit': 300000, 'rate': 0},
    {'limit': 600000, 'rate': 0.05},
    {'limit': 900000, 'rate': 0.10},
    {'limit': 1200000, 'rate': 0.15},
    {'limit': 1500000, 'rate': 0.20},
    {'limit': float('inf'), 'rate': 0.30}
]


# ---------------- SALARY ---------------- #
def get_salary():
    print("\n📊 Enter Salary Details")
    basic = float(input("Basic Salary: ₹"))
    hra = float(input("HRA Received: ₹"))
    other = float(input("Other Allowances: ₹"))
    bonus = float(input("Bonus: ₹"))

    gross = basic + hra + other + bonus
    return basic, hra, gross


# ---------------- HRA ---------------- #
def calculate_hra(basic, hra):
    rent = float(input("Annual Rent Paid: ₹"))
    city = input("Metro city? (yes/no): ").lower()

    percent = 0.5 if city == "yes" else 0.4

    hra_exemption = min(
        hra,
        rent - 0.1 * basic,
        percent * basic
    )

    hra_exemption = max(0, hra_exemption)

    print(f"HRA Exemption: ₹{hra_exemption}")
    return hra_exemption


# ---------------- PROPERTY ---------------- #
def property_details():
    print("\n🏠 Property Details")
    owns = input("Do you own property? (yes/no): ").lower()

    rental_income = 0
    interest = 0

    if owns == "yes":
        rented = input("Is it rented? (yes/no): ").lower()

        if rented == "yes":
            rental_income = float(input("Annual Rental Income: ₹"))

        interest = float(input("Home Loan Interest Paid: ₹"))

    return rental_income, interest


def apply_property(income, rental_income, interest):
    adjusted_rent = rental_income * 0.7  # 30% deduction
    interest_deduction = min(interest, 200000)

    income += adjusted_rent
    income -= interest_deduction

    print("\n🏠 Property Adjustment")
    print(f"Rental Income Added: ₹{adjusted_rent}")
    print(f"Interest Deduction: ₹{interest_deduction}")

    return income


# ---------------- DEDUCTIONS ---------------- #
DEDUCTIONS = [
    {'name': '80C', 'limit': 150000, 'priority': 1},
    {'name': '80D', 'limit': 25000, 'priority': 2},
    {'name': 'NPS', 'limit': 50000, 'priority': 0},
    {'name': 'Education Loan', 'limit': float('inf'), 'priority': 3},
    {'name': 'Donations', 'limit': float('inf'), 'priority': 4},
]


def get_deductions():
    print("\n💸 Enter Investments")
    investments = {}

    for d in DEDUCTIONS:
        try:
            amt = float(input(f"{d['name']} (max ₹{d['limit']}): "))
        except:
            amt = 0
        investments[d['name']] = amt

    return investments


def apply_deductions(income, investments):
    total = 0
    sorted_rules = merge_sort(DEDUCTIONS)

    print("\n[Applying Deductions]")

    for rule in sorted_rules:
        amt = investments.get(rule['name'], 0)
        claim = min(amt, rule['limit'])

        if claim > 0:
            total += claim
            print(f"{rule['name']}: ₹{claim}")

    if total == 0:
        print("(No deductions applied)")

    return total


# ---------------- TAX ---------------- #
def compute_tax(income, slabs):
    tax = 0
    prev = 0

    for slab in slabs:
        if income > slab['limit']:
            tax += (slab['limit'] - prev) * slab['rate']
            prev = slab['limit']
        else:
            tax += (income - prev) * slab['rate']
            break

    return tax


# ---------------- EXPENSE ---------------- #
def expense_analysis(income):
    print("\n📉 Monthly Expenses")

    try:
        rent = float(input("Rent: ₹"))
        food = float(input("Food: ₹"))
        travel = float(input("Travel: ₹"))
        emi = float(input("EMI: ₹"))
    except:
        rent = food = travel = emi = 0

    total = (rent + food + travel + emi) * 12
    savings = income - total

    print(f"Annual Expenses: ₹{total}")
    print(f"Estimated Savings: ₹{savings}")


# ---------------- SUGGESTIONS ---------------- #

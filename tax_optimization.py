# ---------------- MAIN ---------------- #
def main():
    print("\n💰 ADVANCED TAX OPTIMIZATION SYSTEM (INDIA)")

    basic, hra, gross = get_salary()
    hra_exemption = calculate_hra(basic, hra)

    rental_income, interest = property_details()

    investments = get_deductions()
    deduction_total = apply_deductions(gross, investments)

    # OLD REGIME
    taxable_old = gross - deduction_total - hra_exemption
    taxable_old = apply_property(taxable_old, rental_income, interest)

    # NEW REGIME
    taxable_new = gross

    tax_old = compute_tax(taxable_old, OLD_REGIME)
    tax_new = compute_tax(taxable_new, NEW_REGIME)

    print("\n========== RESULTS ==========")
    print(f"Old Regime Tax: ₹{tax_old}")
    print(f"New Regime Tax: ₹{tax_new}")

    if tax_old < tax_new:
        print("👉 Old Regime is Better")
    else:
        print("👉 New Regime is Better")

    expense_analysis(gross)
    suggest(investments)


# RUN
main()

